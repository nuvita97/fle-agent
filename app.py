@app.route("/generate", methods=["POST"])
def generate():
    level = request.form.get("level", "").upper()
    topic = request.form.get("topic", "")

    if level not in VALID_LEVELS or topic not in VALID_TOPICS:
        return redirect(url_for("index"))

    _track_usage("generate", level=level, topic=topic)
    exercise_id = get_next_exercise_id(level, topic)

    if exercise_id is None:
        if DEMO_MODE:
            return render_template("index.html", topics=TOPIC_DISPLAY, topic_icons=TOPIC_ICONS,
                                   levels=VALID_LEVELS,
                                   error=f"Cache introuvable pour {level} / {TOPIC_DISPLAY[topic]}. "
                                         f"Lancez d'abord : python tools/prebuild_cache.py")
        # Production fallback: generate on the fly
        data = run_tools(level, topic)
        if data is None:
            return render_template("index.html", topics=TOPIC_DISPLAY, topic_icons=TOPIC_ICONS,
                                   levels=VALID_LEVELS, error="Erreur lors de la génération. Vérifiez les outils.")
        session["fallback_exercise"] = data
        session["current_exercise_id"] = None
    else:
        session["fallback_exercise"] = None
        session["current_exercise_id"] = exercise_id

    session["level"] = level
    session["topic"] = topic
    session["fun_fact"] = get_random_fact(topic)
    return redirect(url_for("exercise"))


@app.route("/exercise")
def exercise():
    level = session.get("level")
    topic = session.get("topic")
    if not level or not topic:
        return redirect(url_for("index"))
    exercise_id = session.get("current_exercise_id")
    if exercise_id:
        data = load_exercise_by_id(exercise_id)
    else:
        data = session.get("fallback_exercise")
    if not data:
        return redirect(url_for("index"))
    data["topic_display"] = TOPIC_DISPLAY[topic]
    data["fun_fact"] = session.get("fun_fact", "")
    return render_template("exercise.html", data=data)


@app.route("/download")
def download():
    sys.path.insert(0, os.path.join(BASE_DIR, "tools"))
    from build_pdf import build_pdf_bytes  # noqa: PLC0415

    level = request.args.get("level") or session.get("level")
    topic = request.args.get("topic") or session.get("topic")

    if not level or not topic:
        return "Niveau ou thème manquant.", 400

    exercise_id = session.get("current_exercise_id")
    if exercise_id:
        data = load_exercise_by_id(exercise_id)
    else:
        data = session.get("fallback_exercise")
    if not data:
        return "Données d'exercice introuvables.", 404

    exercises = {"questions": data.get("questions", []), "vocabulary": data.get("vocabulary", [])}
    try:
        pdf_bytes = build_pdf_bytes(
            level=level, topic=topic,
            text=data["text"], url=data.get("url", ""), site_name=data.get("site_name", ""),
            exercises=exercises,
            open_question=data.get("open_question", ""),
        )
    except Exception as e:
        app.logger.error(f"PDF error: {e}")
        return "Erreur lors de la génération du PDF.", 500

    buf = io.BytesIO(pdf_bytes)
    buf.seek(0)
    today = datetime.utcnow().strftime("%Y-%m-%d")
    return send_file(buf, as_attachment=True, mimetype="application/pdf",
                     download_name=f"exercice_{level}_{topic}_{today}.pdf")


@app.route("/subscribe", methods=["POST"])
def subscribe():
    name  = request.form.get("name", "").strip()
    email = request.form.get("email", "").strip().lower()
    level = request.form.get("sub_level", "A1").upper()
    topic = request.form.get("sub_topic", "vie_quotidienne")

    if level not in VALID_LEVELS:
        level = "A1"
    if topic not in VALID_TOPICS:
        topic = "vie_quotidienne"

    lang = session.get("lang", "fr")
    t = TRANSLATIONS[lang]

    def _render(sub_error=None, sub_success=None):
        return render_template("index.html", topics=TOPIC_DISPLAY, topic_icons=TOPIC_ICONS,
                               levels=VALID_LEVELS, sub_error=sub_error, sub_success=sub_success)

    if not name or not email:
        return _render(sub_error="Veuillez remplir tous les champs." if lang == "fr"
                       else "Please fill in all fields.")

    if not re.match(r"^[^@\s]+@[^@\s]+\.[^@\s]+$", email):
        return _render(sub_error="Adresse e-mail invalide." if lang == "fr"
                       else "Invalid email address.")

    try:
        sb = _get_supabase()

        existing = sb.table("subscribers").select("email").eq("email", email).execute().data
        if existing:
            return _render(sub_error=t["sub_already"])

        token = str(uuid.uuid4())
        sb.table("subscribers").insert({
            "name": name,
            "email": email,
            "level": level,
            "topic": topic,
            "token": token,
            "subscribed_at": datetime.utcnow().isoformat(),
        }).execute()

    except KeyError:
        app.logger.warning("Supabase env vars not set — subscriber not saved.")
        token = ""
    except Exception as e:
        app.logger.error(f"Supabase error: {e}")
        return _render(sub_error="Erreur technique. Réessayez plus tard." if lang == "fr"
                       else "Technical error. Please try again later.")

    # Send welcome email in background — don't block the HTTP response
    if token:
        import threading
        sys.path.insert(0, os.path.join(BASE_DIR, "tools"))
        from send_email import send_welcome  # noqa: PLC0415
        manage_url, unsubscribe_url = _subscriber_urls(token)

        def _send_welcome_bg():
            try:
                result = send_welcome(
                    recipient=email, name=name, level=level, topic=topic,
                    manage_url=manage_url, unsubscribe_url=unsubscribe_url,
                )
                if "error" in result:
                    app.logger.warning(f"Welcome email failed: {result}")
                else:
                    app.logger.info(f"Welcome email sent to {email}")
            except Exception as e:
                app.logger.warning(f"Welcome email failed: {e}")

        threading.Thread(target=_send_welcome_bg, daemon=True).start()

    success_msg = (f"Merci {name} ! Vous êtes inscrit(e). Vérifiez votre boîte e-mail."
                   if lang == "fr" else
                   f"Thank you {name}! You're subscribed. Check your inbox.")
    return _render(sub_success=success_msg)


@app.route("/manage", methods=["GET", "POST"])
def manage():
    token = request.args.get("token", "")
    if not token:
        return redirect(url_for("index"))

    try:
        sb = _get_supabase()
        rows = sb.table("subscribers").select("name,level,topic").eq("token", token).execute().data
    except Exception:
        return redirect(url_for("index"))

    if not rows:
        return render_template("manage.html", error="Lien invalide ou expiré.",
                               topics=TOPIC_DISPLAY, levels=VALID_LEVELS, token=token)

    sub = rows[0]

    if request.method == "POST":
        new_level = request.form.get("level", sub["level"]).upper()
        new_topic = request.form.get("topic", sub["topic"])
        if new_level not in VALID_LEVELS:
            new_level = sub["level"]
        if new_topic not in VALID_TOPICS:
            new_topic = sub["topic"]
        try:
            sb.table("subscribers").update({"level": new_level, "topic": new_topic}).eq("token", token).execute()
            sub["level"] = new_level
            sub["topic"] = new_topic
            return render_template("manage.html", sub=sub, success=True,
                                   topics=TOPIC_DISPLAY, levels=VALID_LEVELS, token=token)
        except Exception as e:
            app.logger.error(f"Manage update error: {e}")
            return render_template("manage.html", sub=sub, error="Erreur technique.",
                                   topics=TOPIC_DISPLAY, levels=VALID_LEVELS, token=token)

