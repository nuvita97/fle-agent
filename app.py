def load_cached_exercise(level: str, topic: str) -> dict | None:
    """Load a random exercise for a specific level+topic from Supabase.

    Returns a randomly chosen row so users see different content on each visit
    as the exercise pool grows week over week.
    """
    try:
        sb = _get_supabase()
        rows = (
            sb.table("exercises")
            .select("*")
            .eq("level", level)
            .eq("topic", topic)
            .execute()
            .data
        )
        return random.choice(rows) if rows else None
    except Exception:
        return None


def fetch_exercise_ids(level: str, topic: str) -> list[str]:
    """Fetch all exercise IDs for a given level+topic (lightweight, IDs only)."""
    try:
        sb = _get_supabase()
        rows = (
            sb.table("exercises")
            .select("id")
            .eq("level", level)
            .eq("topic", topic)
            .execute()
            .data
        )
        return [row["id"] for row in rows] if rows else []
    except Exception:
        return []


def load_exercise_by_id(exercise_id: str) -> dict | None:
    """Fetch a single full exercise row by UUID."""
    try:
        sb = _get_supabase()
        rows = (
            sb.table("exercises")
            .select("*")
            .eq("id", exercise_id)
            .limit(1)
            .execute()
            .data
        )
        return rows[0] if rows else None
    except Exception:
        return None


def get_next_exercise_id(level: str, topic: str) -> str | None:
    """Return next exercise ID from the non-repeating shuffled queue.

    Session structure:
        session["lesson_queues"] = {
            "A1_vie_quotidienne": {"order": ["uuid1", ...], "index": 0}
        }
    - First call: fetches all IDs, shuffles once, stores order, returns first.
    - Subsequent calls: advances index circularly (wraps after exhaustion).
    """
    queue_key = f"{level}_{topic}"
    queues = session.get("lesson_queues", {})
    queue = queues.get(queue_key)

    if not queue:
        ids = fetch_exercise_ids(level, topic)
        if not ids:
            return None
        random.shuffle(ids)
        queue = {"order": ids, "index": 0}

    order = queue["order"]
    if not order:
        return None

    idx = queue["index"] % len(order)   # circular wrap
    exercise_id = order[idx]
    queue["index"] = idx + 1

    queues[queue_key] = queue
    session["lesson_queues"] = queues
    session.modified = True             # required for nested dict mutations
    return exercise_id


def load_latest_exercise(level: str, topic: str) -> dict | None:
    """Load the most recently generated exercise for a specific level+topic.

    Used by the newsletter to always send the freshest content from this week's batch.
    """
    try:
        sb = _get_supabase()
        rows = (
            sb.table("exercises")
            .select("*")
            .eq("level", level)
            .eq("topic", topic)
            .order("created_at", desc=True)
            .limit(1)
            .execute()
            .data
        )
        return rows[0] if rows else None
    except Exception:
        return None


def find_best_cached_exercise(level: str, topic: str) -> tuple:
    """Return the latest exercise for exact level+topic, or fall back to same topic any level.

    Used by the newsletter — always picks the most recently generated content.
    """
    data = load_latest_exercise(level, topic)
    if data:
        return data, level, topic
    # Fallback: same topic, any level (also pick latest)
    try:
        sb = _get_supabase()
        for lvl in VALID_LEVELS:
            rows = (
                sb.table("exercises")
                .select("*")
                .eq("level", lvl)
                .eq("topic", topic)
                .order("created_at", desc=True)
                .limit(1)
                .execute()
                .data
            )
            if rows:
                return rows[0], lvl, topic
        # Last resort: any exercise (latest overall)
        rows = (
            sb.table("exercises")
            .select("*")
            .order("created_at", desc=True)
            .limit(1)
            .execute()
            .data
        )
        if rows:
            r = rows[0]
            return r, r["level"], r["topic"]
    except Exception:
        pass
    return None, level, topic


def run_tools(level: str, topic: str) -> dict | None:
    """Call generate_lesson tool as subprocess. Used in production mode (non-demo, cache miss)."""
    python = sys.executable
    result = subprocess.run(
        [python, "tools/generate_lesson.py", "--level", level, "--topic", topic],
        capture_output=True, text=True,
    )
    if result.returncode != 0:
        return None
    try:
        return json.loads(result.stdout)
    except json.JSONDecodeError:
        return None


def _get_supabase():
    from supabase import create_client  # noqa: PLC0415
    return create_client(os.environ["SUPABASE_URL"], os.environ["SUPABASE_KEY"])


def _track_usage(event_type: str, level: str = "", topic: str = "") -> None:
    """Fire-and-forget usage tracking write to Supabase.

    Runs in a background thread so it never adds latency to the HTTP response.
    Failures are logged as warnings but never raise to the caller.
    """
    import threading

    def _write():
        try:
            sb = _get_supabase()
            sb.table("usage_events").insert({
                "event_type": event_type,
                "level": level or None,
                "topic": topic or None,
            }).execute()
        except Exception as exc:
            app.logger.warning(f"Usage tracking write failed: {exc}")

    threading.Thread(target=_write, daemon=True).start()


def _subscriber_urls(token: str) -> tuple[str, str]:
    manage_url = f"{PUBLIC_URL}/manage?token={token}"
    unsubscribe_url = f"{PUBLIC_URL}/unsubscribe?token={token}"
    return manage_url, unsubscribe_url


