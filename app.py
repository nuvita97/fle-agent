def send_newsletter_to_all() -> dict:
    """Fetch all subscribers and send each one a personalized exercise email."""
    sys.path.insert(0, os.path.join(BASE_DIR, "tools"))
    from build_pdf import build_pdf_bytes   # noqa: PLC0415
    from send_email import send as email_send  # noqa: PLC0415

    results = {"sent": [], "failed": [], "skipped": []}

    try:
        sb = _get_supabase()
        rows = sb.table("subscribers").select("name,email,level,topic,token").execute().data
    except Exception as e:
        app.logger.error(f"Newsletter: could not fetch subscribers: {e}")
        return {"error": str(e)}

    for row in rows:
        email   = row.get("email", "")
        name    = row.get("name", "")
        level   = row.get("level") or "A1"
        topic   = row.get("topic") or "vie_quotidienne"
        token   = row.get("token", "")

        data, actual_level, actual_topic = find_best_cached_exercise(level, topic)
        if not data:
            app.logger.warning(f"Newsletter: no cache found for {email} ({level}/{topic}) — skipping.")
            results["skipped"].append(email)
            continue

        try:
            pdf_bytes = build_pdf_bytes(
                level=actual_level, topic=actual_topic,
                text=data["text"], url=data.get("url", ""), site_name=data.get("site_name", ""),
                exercises={"questions": data.get("questions", []), "vocabulary": data.get("vocabulary", [])},
                open_question=data.get("open_question", ""),
            )
        except Exception as e:
            app.logger.error(f"Newsletter: PDF error for {email}: {e}")
            results["failed"].append(email)
            continue

        # Write to a temp file (send_email expects a file path)
        import tempfile
        with tempfile.NamedTemporaryFile(suffix=".pdf", delete=False) as tmp:
            tmp.write(pdf_bytes)
            tmp_path = tmp.name

        today = datetime.utcnow().strftime("%Y-%m-%d")
        desired_filename = f"exercice_{actual_level}_{actual_topic}_{today}.pdf"

        manage_url, unsubscribe_url = _subscriber_urls(token)
        result = email_send(
            pdf_path=tmp_path,
            level=actual_level,
            topic=actual_topic,
            source_url="",
            source_name="",
            recipient=email,
            manage_url=manage_url,
            unsubscribe_url=unsubscribe_url,
            recipient_name=name,
            pdf_filename=desired_filename,
        )
        os.unlink(tmp_path)

        if "error" in result:
            app.logger.error(f"Newsletter: send failed for {email}: {result}")
            results["failed"].append(email)
        else:
            results["sent"].append(email)

    app.logger.info(f"Newsletter done — sent: {len(results['sent'])}, "
                    f"failed: {len(results['failed'])}, skipped: {len(results['skipped'])}")
    return results


